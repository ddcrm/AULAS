    <h2>
    <p>O que é Framework? <p>
    <h2>

   <h4> O framework de desenvolvimento é uma ferramenta muito usada pelos programadores. Ele é, basicamente, uma plataforma que contém um grupo de códigos que ajudam a desenvolver sites, aplicativos, entre outros tipos de sistemas.<br/>

<br/>Sendo assim, o framework irá agir como um molde, que pode ser alterado de acordo com o que o programador deseja para, assim, construir os sistemas e softwares já citados acima.

Além disso, é interessante destacar que o framework de desenvolvimento pode ser dividido entre front-end e back-end.<br/>

<br/>Os frameworks de back-end são ótimos para serem usados em projetos que envolvem usuários e servidores, uma vez que envolve banco de dados, armazenamentos e segurança . Ou seja, toda a parte interna de uma programação.

Já o framework de front-end é mais utilizado para desenvolver programas que possuem HTML5. Esse tipo de framework está ligado a interface da programação, ou seja, toda a parte estrutural, a aparência, que pode ser vista, usada e explorada por um visitante de site ou aplicativo.<br/><h4>

    <h2><p> Vantagens <p></h2>

    <h4>
    Além de ser muito eficaz na criação de sites e aplicativos, sem deixar que o programador precise passar pelo processo de realizar um trabalho do zero – o que demora muito – o framework de desenvolvimento ainda possui outras vantagens. Como por exemplo:
    <h4>

    <h4>Um tempo que deixa de ser gasto desnecessariamente dá a um profissional a chance de aplicá-lo a algo que pode estar necessitando de uma maior atenção.

    Ao construir aplicações com maior rapidez e fluidez, a produtividade de um programador ou programadora também se beneficia, o tornando apto a fazer mais em menor tempo – sem abrir mão da qualidade de seu trabalho.</h4>

    <h4>
    Ao utilizar um framework, as chances que você tem de se deparar com erros frequentes são consideravelmente reduzidas.

    Isso porque todas possíveis falhas deste código já foram identificadas e resolvidas pelos desenvolvedores responsáveis pela criação do framework escolhido, cujo objetivo é justamente evitar que se incorra novamente nestes mesmos desvios. </h4>

    <h4>Um framework é um código padronizado, portanto há uma manutenção padronizada também. Na hora de fazer uma melhoria ou consertar algum ponto de atenção, não será necessário quebrar a cabeça em busca de uma solução desconhecida: basta acessar a documentação do framework e encontrar a resposta. </h4>


    <h4>Com uma menor chance de erros de execução e uma manutenção simples, os frameworks são tecnologias que garantem mais segurança ao desenvolvimento de softwares, tanto para os profissionais de TI quanto para seus clientes.</h4>

    <h4>Projetos padronizados tem como uma de suas maiores vantagens a reusabilidade. Ou seja: ao ter familiaridade com diferentes tipos de frameworks, uma empresa prestadora de serviços pode padronizar suas entregas e seus projetos de modo que todos eles corram com a maior previsibilidade possível. Tornando assim todo o processo mais favorável à equipe. Se tempo é dinheiro, a economia de tempo também pode ser encarada como uma economia financeira. Ao obter um melhor aproveitamento do time de profissionais e de sua produtividade, os custos de manutenção de um projeto também caem.</H4>
    
    <H4>Se uma aplicação poderia demorar 6 meses para ser totalmente desenvolvida, com o uso de um framework podemos contar que este tempo pode ser encurtado em pelo menos 50%.

    Sob o ponto de vista comercial, esta é uma grande vantagem competitiva: enquanto seu concorrente é capaz de entregar 2 projetos ao ano, você será capaz de entregar 4.</H4>

    <H2><P>Desvantagens</P></H2>

    <h4>Como em tecnologia da informação nem tudo são flores, os frameworks também possuem algumas desvantagens técnicas. Que são:</h4>

    <h4>Dependencia, recomendamos fortemente que os frameworks sejam utilizados por profissionais experientes que já possuam um razoável grau de familiaridade tanto com a aplicação desenvolvida quanto com a linguagem de programação que este framework usa. Isso porque, na posição de estudante, ingressar no mercado de trabalho muito habituado ao uso de frameworks pode te impedir de adquirir importantes capacidades, principalmente àquelas relacionadas à resolução de problemas. </h4>
    <h4>Por utilizar códigos fixos e imutáveis, muitas vezes os frameworks podem apresentar algumas dificuldades de configuração. Isso acontece, principalmente, quando os profissionais envolvidos em seu uso não possuem familiaridade com suas tecnologias.

    Caso este obstáculo se apresente, recomendamos a leitura atenta da documentação deste framework e a interação com outros profissionais da comunidade.</h4>

    <h4>Pode acontecer, também, de utilizarmos um framework inteiro para desenvolver uma aplicação que precisa somente de uma parte das funções oferecidas por ele.

Neste caso, os códigos que "sobrarem" poderão acarretar um peso desnecessário ao projeto.

Se a aplicação desenvolvida for um site, por exemplo, é preciso tomar cuidado para que este código adicional não represente maior tempo de carregamento das páginas.</h4>
